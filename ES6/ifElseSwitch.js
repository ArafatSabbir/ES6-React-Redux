var color = "blue";
if (color == "red") {
  console.log("Color is red");
} else if (color == "blue") {
  console.log("Color is blue");
} else {
  console.log("Color is not red or blue");
}

// switch statement
var name = "John";
switch (name) {
  case "John":
    console.log("Your name is John");
    break;
  case "Mary":
    console.log("Your name is Mary");
    break;
  default:
    console.log("Your name is not John or Mary");
}
